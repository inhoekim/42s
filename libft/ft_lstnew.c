#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_list;

	new_list = (t_list *)malloc(sizeof(t_list));
	if (new_list == FT_NULL)
		return (FT_NULL);
	new_list->content = content;
	new_list->next = FT_NULL;
	return (new_list);
}
